
# Python Basics
# Number Functions in Python

# Basic Arithmetic Operations
a = 10
b = 3
addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b
remainder = a % b
exponentiation = a ** b
print(addition, subtraction, multiplication, division, remainder, exponentiation)

# Absolute Value
absolute = abs(-7.5)
print(absolute)

# Rounding
rounded_up = round(9.75)
rounded_down = round(9.25)
print(rounded_up, rounded_down)

# Conversion Functions
int_value = int(5.67)
float_value = float("3.14")
str_value = str(123)
print(int_value, float_value, str_value)

# Maximum and Minimum
max_value = max(10, 20, 5)
min_value = min(8, 18, 2)
print(max_value, min_value)

# Mathematical Functions (from the math module)
import math

sqrt = math.sqrt(25)
ceil = math.ceil(4.3)
floor = math.floor(4.9)
sin_value = math.sin(math.radians(30))
cos_value = math.cos(math.radians(60))
print(sqrt, ceil, floor, sin_value, cos_value)
