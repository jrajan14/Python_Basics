# Python Basic Programs
# Arithmatics in Python

a = 20
print ("Value of A = ", a) #Note that a comma is used to concat the Mentioned string and value of variable

b = 10
print ("Value of B = ", b)

c = a + b               # Addition
print ("A + B = ", c)

d = a - b               # Subtraction
print ("A - B = ", d)

e = a * b               # Multiplication
print ("A x B = ", e)

f = a / b               # Division
print ("A / B = ", f)

###########################################

g = a + 10             # Addition of variable and numeric value
print ("G = ",g)

h = "a" + "b"          # Concat two strings
print ("H = ",h)

i = str(a) + str(b)    # Concat values of a and b
print ("I = ",i)